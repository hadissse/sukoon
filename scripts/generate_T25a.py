def word_count_ar(text):
    # Arabic word count using whitespace as separator
    return len([w for w in text.replace("،", " ").replace(".", " ").split() if w.strip()])


def main():
    rows_out = []
    with INPUT.open("r", encoding="utf-8", newline="") as f:
        reader = csv.DictReader(f)
        fieldnames = reader.fieldnames
        for row in reader:
            sign_label = row["body_b_or_sign"].strip()
            house_label = row["house_or_aspect"].strip()
            sign_data = SIGNS[sign_label]
            house_data = HOUSES[house_label]
            trad, evo, dev = render(sign_label, sign_data, house_label, house_data)
            row["traditional_meaning_ar"] = trad
            row["evolutionary_meaning_ar"] = evo
            row["developmental_task_ar"] = dev
            row["key_references"] = sign_data["ref_voice"]
            row["status"] = "filled"
            total = word_count_ar(trad) + word_count_ar(evo) + word_count_ar(dev)
            # sanity: ensure within 220-320 band; we'll print warnings
            if total < 220 or total > 320:
                print(f"WARN {row['placement_id']}: total words = {total} "
                      f"(t={word_count_ar(trad)}, e={word_count_ar(evo)}, d={word_count_ar(dev)})")
            rows_out.append(row)

    with OUTPUT.open("w", encoding="utf-8", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(rows_out)

    print(f"Wrote {len(rows_out)} rows to {OUTPUT}")


if __name__ == "__main__":
    main()
