// Sort chronologically; ties: phases before transits
UNIFIED_TIMELINE.sort((a, b) => {
  if (a.sortAge !== b.sortAge) return a.sortAge - b.sortAge;
  if (a.type !== b.type) return a.type === 'phase' ? -1 : 1;
  return 0;
});

