function norm360(d: number): number {
  return ((d % 360) + 360) % 360;
}

function lonToSignDeg(lon: number): string {
  const n = norm360(lon);
  const sign = Math.floor(n / 30);
  const deg = Math.floor(n % 30);
  return `${ZODIAC_AR[sign]} ${toAr(deg)}°`;
}

