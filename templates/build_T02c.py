def main():
    with SRC.open("r", encoding="utf-8", newline="") as f:
        reader = csv.DictReader(f)
        fieldnames = reader.fieldnames
        rows = list(reader)

    if len(rows) != 36:
        raise SystemExit(f"Expected 36 rows in source, got {len(rows)}")

    missing = [r["placement_id"] for r in rows if r["placement_id"] not in CONTENT]
    if missing:
        raise SystemExit(f"Missing content for: {missing}")

    for r in rows:
        c = CONTENT[r["placement_id"]]
        r["traditional_meaning_ar"] = c["trad"]
        r["evolutionary_meaning_ar"] = c["evo"]
        r["developmental_task_ar"] = c["task"]
        r["key_references"] = c["refs"]
        r["status"] = "filled"

    DST.parent.mkdir(parents=True, exist_ok=True)
    with DST.open("w", encoding="utf-8", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(rows)

    # Verification
    with DST.open("r", encoding="utf-8", newline="") as f:
        reader = csv.DictReader(f)
        out_rows = list(reader)

    # Word counts and forbidden checks
    forbidden_tokens = ["Uranus", "Neptune", "Pluto", "—", "Steiner", "Anthroposoph", "المسيح"]
    issues = []
    for r in out_rows:
        combined = " ".join([r["traditional_meaning_ar"], r["evolutionary_meaning_ar"], r["developmental_task_ar"]])
        wc = len(combined.split())
        if wc < 200 or wc > 350:
            issues.append(f"{r['placement_id']}: word count {wc}")
        for tok in forbidden_tokens:
            if tok in combined:
                issues.append(f"{r['placement_id']}: contains forbidden '{tok}'")
        # check 'not X but Y' Arabic patterns - rough check
        if "ليس X بل Y" in combined:
            issues.append(f"{r['placement_id']}: contains 'not X but Y' pattern")

    print(f"Output rows: {len(out_rows)}")
    print(f"Path: {DST}")
    if issues:
        print("ISSUES:")
        for i in issues:
            print(" -", i)
    else:
        print("All checks passed.")


if __name__ == "__main__":
    main()
