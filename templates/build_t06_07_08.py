def build_rows(src_rows):
    out = []
    for row in src_rows:
        tnum = row["template_num"]
        tname = row["template_name"]
        sign = row["body_a"].strip()
        house = row["house_or_aspect"].strip()

        if tnum == "6" and tname == "Stellium in Sign":
            d = STELLIUM_SIGN[sign]
        elif tnum == "6" and tname == "Stellium in House":
            d = STELLIUM_HOUSE[house]
        elif tnum == "7":
            d = EMPTY_HOUSE[house]
        elif tnum == "8":
            d = INTERCEPTED[sign]
        else:
            raise ValueError(f"Unmatched row: {row}")

        row["traditional_meaning_ar"] = d["trad"]
        row["evolutionary_meaning_ar"] = d["evo"]
        row["developmental_task_ar"] = d["task"]
        row["key_references"] = d["refs"]
        row["status"] = "filled"
        out.append(row)
    return out


def main():
    with SRC.open("r", encoding="utf-8", newline="") as f:
        reader = csv.DictReader(f)
        fieldnames = reader.fieldnames
        rows = list(reader)

    filled = build_rows(rows)
    assert len(filled) == 48, f"expected 48 rows, got {len(filled)}"

    # word-count sanity
    issues = []
    for r in filled:
        total = sum(
            len(r[col].split())
            for col in ("traditional_meaning_ar", "evolutionary_meaning_ar", "developmental_task_ar")
        )
        if total < 200 or total > 350:
            issues.append((r["placement_id"], total))
    if issues:
        print("WARNING word-count out of range:", issues)

    # em-dash check in prose fields
    em_issues = []
    for r in filled:
        for col in ("traditional_meaning_ar", "evolutionary_meaning_ar", "developmental_task_ar"):
            if "—" in r[col]:
                em_issues.append((r["placement_id"], col))
    if em_issues:
        print("WARNING em-dash found:", em_issues)

    DST.parent.mkdir(parents=True, exist_ok=True)
    with DST.open("w", encoding="utf-8", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(filled)

    print(f"Wrote {len(filled)} rows to {DST}")


if __name__ == "__main__":
    main()
