    refs = []
    if "زحل" in (a, b):
        refs.append("Greene — Saturn: A New Look at an Old Devil")
    if "كيرون" in (a, b):
        refs.append("Reinhart — Chiron and the Healing Journey")
    if "بلوتو" in (a, b) or "العقدة الشمالية" in (a, b) or "العقدة الجنوبية" in (a, b):
        refs.append("Arroyo — Astrology, Karma & Transformation")
    # base aspect refs — pick 1 or 2 from general pool deterministically
    seed = hash(f"{a}|{b}") & 0xFFFF
    rnd = random.Random(seed)
    pool = list(REF_POOL_GENERAL)
    rnd.shuffle(pool)
    for r in pool:
        if r not in refs:
            refs.append(r)
        if len(refs) >= 3:
            break
    return "; ".join(refs[:3])


