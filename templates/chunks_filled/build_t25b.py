def pick_refs(sign_key, house_num):
    # Choose 2-3 refs varied by house index
    refs = SIGNS[sign_key]["refs"]
    # Greene Saturn on most rows; rotate 2 others
    idx2 = house_num % len(refs)
    idx3 = (house_num + 2) % len(refs)
    chosen = ["Greene, Saturn"]
    for r in (refs[idx2], refs[idx3]):
        if r not in chosen:
            chosen.append(r)
        if len(chosen) >= 3:
            break
    return "; ".join(chosen)


def main():
    rows = []
    with SRC.open("r", encoding="utf-8", newline="") as f:
        reader = csv.DictReader(f)
        fieldnames = reader.fieldnames
        for row in reader:
            sign = row["body_b_or_sign"].strip()
            house_label = row["house_or_aspect"].strip()
            # Extract house number from Arabic label like "البيت 1"
            house_num = int(house_label.split()[-1])
            row["traditional_meaning_ar"] = build_traditional(sign, house_num)
            row["evolutionary_meaning_ar"] = build_evolutionary(sign, house_num)
            row["developmental_task_ar"] = build_task(sign, house_num)
            row["key_references"] = pick_refs(sign, house_num)
            row["status"] = "filled"
            rows.append(row)

    with DST.open("w", encoding="utf-8", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(rows)

    # Verify
    print(f"Wrote {len(rows)} rows to {DST}")
    # Word count check on first row
    sample = rows[0]
    total_words = sum(
        len(sample[c].split())
        for c in ("traditional_meaning_ar", "evolutionary_meaning_ar", "developmental_task_ar")
    )
    print(f"Sample row 1 ({sample['placement_id']}) total Arabic word count: {total_words}")
    # Check all rows for word count range
    out_of_range = []
    for r in rows:
        wc = sum(
            len(r[c].split())
            for c in ("traditional_meaning_ar", "evolutionary_meaning_ar", "developmental_task_ar")
        )
        if wc < 200 or wc > 350:
            out_of_range.append((r["placement_id"], wc))
    if out_of_range:
        print(f"OUT OF RANGE: {out_of_range}")
    else:
        print("All rows within 200-350 word range.")


if __name__ == "__main__":
    main()
