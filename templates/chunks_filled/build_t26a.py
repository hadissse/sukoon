def pick_refs(sign, house_num):
    """Rotate references based on sign and house to avoid repetition."""
    pool = SIGN_DATA[sign]["refs"]
    # Use house_num to pick different subsets
    n = len(pool)
    start = (house_num - 1) % n
    indices = [(start + i) % n for i in range(3)]
    selected = [pool[i] for i in indices]
    return "; ".join(selected)


