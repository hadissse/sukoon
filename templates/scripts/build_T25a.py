def build_row(sign, house):
    s = SIGN_DATA[sign]
    h = HOUSE_DATA[house]

    trad = (
        f"{s['trad_open']} يهبط زحل هنا في {h['domain']}. "
        f"{h['trad_focus']}"
    )

    evo = (
        f"على الطبقة العميقة، {s['evo_root']} "
        f"وفي ميدان هذا البيت تحديداً، {h['evo_layer']}"
    )

    task = (
        f"المهمة هنا {h['task_focus']} "
        f"الخطر المتصلب يظهر في {s['rigid']} "
        f"الخطر الوهمي يظهر في {s['inflated']} "
        f"التوازن الحي يتحقق حين {s['balance']} الأنا الواعية الحرة هنا تتعلم أن بنية زحل الحقيقية تحمل الإنسان لا تسجنه."
    )

    base_refs = s["ref_default"]
    hint = h["ref_hint"]
    parts = [p.strip() for p in (base_refs + "; " + hint).split(";")]
    seen = []
    for p in parts:
        if p and p not in seen:
            seen.append(p)
        if len(seen) == 3:
            break
    refs = "; ".join(seen)

    return trad.strip(), evo.strip(), task.strip(), refs


def main():
    with open(SRC, newline="", encoding="utf-8") as f:
        reader = csv.reader(f)
        rows = list(reader)

    header = rows[0]
    body = rows[1:]

    idx = {name: i for i, name in enumerate(header)}

    filled = [header]
    for row in body:
        sign = row[idx["body_b_or_sign"]]
        house = row[idx["house_or_aspect"]]
        trad, evo, task, refs = build_row(sign, house)
        new_row = list(row)
        new_row[idx["traditional_meaning_ar"]] = trad
        new_row[idx["evolutionary_meaning_ar"]] = evo
        new_row[idx["developmental_task_ar"]] = task
        new_row[idx["key_references"]] = refs
        new_row[idx["status"]] = "filled"
        filled.append(new_row)

    dirpath = os.path.dirname(DST)
    fd, tmp_path = tempfile.mkstemp(prefix=".T25a_", suffix=".csv.tmp", dir=dirpath)
    try:
        with os.fdopen(fd, "w", newline="", encoding="utf-8") as f:
            writer = csv.writer(f, quoting=csv.QUOTE_MINIMAL)
            writer.writerows(filled)
        os.replace(tmp_path, DST)
    except Exception:
        try:
            os.unlink(tmp_path)
        except FileNotFoundError:
            pass
        raise

    with open(DST, newline="", encoding="utf-8") as f:
        reader = csv.reader(f)
        out_rows = list(reader)
    data_rows = out_rows[1:]
    print(f"Wrote {DST}")
    print(f"Total data rows: {len(data_rows)}")

    for sample_i in (0, 11, 23, 35, 47, 59, 71):
        r = data_rows[sample_i]
        t = r[idx["traditional_meaning_ar"]]
        e = r[idx["evolutionary_meaning_ar"]]
        d = r[idx["developmental_task_ar"]]
        words = len(t.split()) + len(e.split()) + len(d.split())
        print(f"Row {sample_i} {r[idx['placement_id']]}: total words = {words} "
              f"(trad={len(t.split())}, evo={len(e.split())}, task={len(d.split())})")


if __name__ == "__main__":
    main()
